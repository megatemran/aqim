// Screenshot slider auto-scroll
const screenshotsSlider = document.querySelector('.screenshots-slider');
let isScrolling = false;

if (screenshotsSlider) {
    // Auto-scroll functionality
    let scrollAmount = 0;
    const scrollStep = 1;
    const maxScroll = screenshotsSlider.scrollWidth - screenshotsSlider.clientWidth;

    setInterval(() => {
        if (!isScrolling && maxScroll > 0) {
            scrollAmount += scrollStep;
            if (scrollAmount >= maxScroll) {
                scrollAmount = 0;
            }
            screenshotsSlider.scrollLeft = scrollAmount;
        }
    }, 30);

    // Pause auto-scroll on hover
    screenshotsSlider.addEventListener('mouseenter', () => {
        isScrolling = true;
    });

    screenshotsSlider.addEventListener('mouseleave', () => {
        isScrolling = false;
    });

    // Pause auto-scroll on touch
    screenshotsSlider.addEventListener('touchstart', () => {
        isScrolling = true;
    });

    screenshotsSlider.addEventListener('touchend', () => {
        setTimeout(() => {
            isScrolling = false;
        }, 2000);
    });
}

